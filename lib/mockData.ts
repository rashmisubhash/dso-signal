export function mockInvoicesAsCSV(): string {
  const header = "invoice_id,customer,amount_cents,issued_date,due_date,paid_date,status";
  const rows = mockInvoices.map((inv) => {
    const customer = inv.customer.includes(",") ? `"${inv.customer}"` : inv.customer;
    return [
      inv.invoice_id,
      customer,
      inv.amount_cents,
      inv.issued_date,
      inv.due_date,
      inv.paid_date ?? "",
      inv.status,
    ].join(",");
  });
  return [header, ...rows].join("\n");
}
